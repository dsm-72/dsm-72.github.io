    ## Account Threads
    What does "ownership of the account's heart is gifted to the agent W0 in totality and barring liability" actually mean, *in spiritu*? To answer this, let us first build intuition by considering the day-to-day of an account with W0 ownership of the AT. In essence, everything is the same. After all, the gift is *in vivo* and it is up to the account to use the allocated AT as they wish, without W0 being liable for the accounts actions. 
    
    Why gift the AT then? Consider all the factors outside of the account's control that can impact the AT for which they are held liable. For example, the account is liable for maintenance. While the energy requirements of the AT can vary for a variety of reasons, for simplicity let us consider solely energy acquisition. The sources of energy an account has access to is geo-blocked. There is a duration, at any time, that an AT can function on reserves without energy input. As account activity is a serious drain on energy – amongst other account resources - for any point of time there is a not only a fix duration before forced account forfeiture, by extension a fixed distance an account can travel in pursuit of energy income. Further not all energy sources are the same. Decidedly some energy sources are referred to colloquially as "clean". 
    
    While W0 is not liable for energy acquisition on behalf of the account, or even energy accessibility, as owner of the AT, it is of W0's interest to ensure energy sources are available, and especially that those available are non-corrosive. Although the account has independent action, including liability for the AT's use, as owner W0 may take legal action against third parties which persuade, pursue, mislead, or otherwise make deleterious energy sources favorable for the account (leading them to choose to take them).
    
